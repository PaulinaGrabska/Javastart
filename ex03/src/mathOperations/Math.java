package mathOperations;


public class Math {


    public boolean isEven(int a){
        return a%2==0;
    }

    public boolean isOdd(int a){
        return a%2!=0;
    }

    public double circleField(double radius){
        return radius*radius* java.lang.Math.PI;
    }

    public int power(int a){
        return a*a;
    }

}
